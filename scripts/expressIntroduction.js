// Libraries vs. Frameworks

// Library, when you use one, you are in charge!
// You control the flow of the application code,
// and you decide when to use the library.
// for ex: axios, color generators, jQuery

// With Frameworks, the control is inverted.
// The framework is in charge, and you are merely a participant!
// The framework tells you where to plug in your code.
// for ex: express, ruby on rails,
